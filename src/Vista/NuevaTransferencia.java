    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        java.util.Date utilDate = new java.util.Date();
        java.sql.Date sqlDate = convert(utilDate);
        String a = jTextField1.getText();
        String x = jTextField2.getText();
        char[] b = jPasswordField1.getPassword();
        String c = new String(b);
        if (!c.equals(usr.returnContr())) {
            JOptionPane.showMessageDialog(null, "Contraseña incorrecta");
            return;
        }
        long valor = Long.parseLong(x);
        Transaccion t = new Transaccion(usr.returnNombre(), sqlDate, "ingreso", valor, a, usr);
        t.nuevaTransaccion();
        
        Transaccion d = new Transaccion(usr.returnNombre(), sqlDate, "retirada", valor, usr.returnDNI(), usr);
        d.nuevaTransaccion();
        
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed


